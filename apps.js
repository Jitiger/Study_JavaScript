//계산기 만들기 코드

const calculator={
    plus:function(a,b){
        console.log(a+b);
    },
    minus:function(a,b){
        console.log(a-b);
    },
    divide:function(a,b){
        console.log(a/b);
    },
    multi:function(a,b){
        console.log(a*b);
    },
    square:function(a,b){
        console.log(a**b);
    },
};
calculator.plus(3,5);
calculator.minus(3,5);
calculator.divide(3,5);
calculator.multi(3,5);
calculator.square(3,5);



